const mongoose = require('mongoose');

const subjectSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  name: {
    type: String,
    required: [true, 'Please provide a subject name'],
    trim: true
  },
  color: {
    type: String,
    default: '#3B82F6'
  },
  description: {
    type: String,
    trim: true
  },
  totalStudyTime: {
    type: Number,
    default: 0
  }
}, {
  timestamps: true
});

module.exports = mongoose.model('Subject', subjectSchema);